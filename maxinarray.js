
// Write a function that takes in an array of numbers and returns the largest number

function findMax(array) {
    result = 0
    for (number of array) {
        if (number > result) {
            result = number
        } else {
            return result
        }

    }
    console.log(result)
}
console.log(findMax([1, 2, 3, 4, 5, 6, 7, 8, 9, 99]))
